const GameBoard = (() => {
  let gameboard = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
  ];

  const clearGameboard = () => {
    gameboard = [
      [1, 2, 3],
      [4, 5, 6],
      [7, 8, 9],
    ];
  };

  const setChar = (x, y, val) => {
    gameboard[x][y].push(val);
  };

  return {
    gameboard,
    clearGameboard,
    setChar,
  };
})();
